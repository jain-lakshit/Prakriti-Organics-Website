        <h2>Om Prakash Ji</h2>
        <h3>Jan Swasthya Sahyog</h3><br>
        <p>
            Om Prakash Ji has done commendable work for natural seed conservation & upgradation through both indigenous & scientific processes- something many generations will remember him for! 
            Some of these seeds are rare and highly needed as per current conditions. These seeds are more resilient to adverse climatic conditions and immune to a lot of ecological problems. 
            Along with it, his experience of organic farming and distribution of native (desi) seeds helps a lot in organic farming. Hundreds of farmers from many villages in the area have benefited from this amazing experiment.
             His initiatives are aimed at making the farmer átmanirbhar or self-reliant’ for seeds/other resources and he also trains them on concepts & methods of natural farming.
        </p>
        
        <div class="row justify-content-left align-items-start pt-5"><h4> As per him, there are multiple benefits of seed conservation – </h4></div>
    
        <div class="row pt-4 pb-4 ">   
                <ul>
                    <li>Self-reliance on inputs (HY seeds are costly and more often can’t be saved and multiplied for further usage as seeds, they can only be used for one time yield)</li>
                    <li>Indigenous seeds have been through the rigorous process of evolution and are adaptable to climatic changes</li>
                    <li>These seeds give yield with better taste and nutritive value</li>
                    <li>They are good for mixed farming practice</li>
                    <li>By-products of plants from these seeds provide good quality fodder and green compost</li>
                </ul>
        </div>


        <p>
            Om Prakash Ji & his team use germplasm to save seeds for a longer period of time (instead of 1-2 years it can be saved for 8-9 years in germ plasm solution). He says germplasm provides us with a system where a chemical is prepared and when that chemical is transferred to seed, the seed does not perish in a short time, it increases the seed's life span. He further added that seed conservation does not require large space. 100 varieties only requires 7-10 decimals. But if we want to maintain the purity and increase seeds, more space is required – 1 variety – 1metric space.  He has saved more than 400 varieties of paddy seeds and many varieties of vegetables, pulses etc. He has done it with help from Jan Swasthya Sahyog - an organization started by 4 sensitive and enthusiastic doctors from AIIMS to provide free & affordable treatment to villagers. 
        </p>

        <p>
            Seed distribution was started by the organization when they came across cases of malnutrition and found out that this can be treated locally with the help of home-grown vegetables and pulses. That’s how this work was started in 2002. Since then, they distribute seeds to farmers by organising an OPD. Om Prakash Ji regularly distributes seeds to hundreds of farmers from 70 villages which JSS has undertaken for health services. He says - “Diversity in farming should be reintroduced. We grow flax seeds( alsi), mustard plant(sarso), teevra, dal and oil. And farmers can do it too! It fulfills our needs for carbohydrates, fats and proteins along with many micronutrients. This way various varieties of crops can be obtained and they have various health benefits. And, the remains can be eaten by animals.”
        </p>

        <p>
            “He gets his inspiration from many people like Dr. Richharia - an agri scientist who devoted his entire life to understanding and augment this centuries old tradition of seed conservation. Another person who has inspired and helped Om Prakash ji is Mr. Jacob from Kerala who travels across India to publicise organic farming and train farmers. His father is also a very big role model for him as he has voluntarily served the health sector for a long period of time and also assisted & motivated him for his work related to seed conservation.
        </p>

