        <h2>Kalpesh Varu</h2>
        <h3>Organic Farmer</h3><br>
        <p>
            Born and brought up in Raipur, Mr. Kalpesh Veru is involved in the business of Organic
Farming. He completed his Diploma in Mechanical Engineering and Post Diploma in
Pollution control engineering and worked at the industries for two years. After working
closely with the industries, he realised that there was a lot of corruption and a lot of
pollution linked with them. The industries worked against the principles of his life and were
causing harm to the Earth. So finally he left his job and started looking for other works. Then
he realised that since farming was a noble job and was practised by his family for years, he
should be involved in it too. After joining the field of agriculture, Mr. Kalpesh started
applying his knowledge of pollution control to stop the pollution in farms. He found that
there was a large amount of pollution linked with agriculture which was a scenario similar to
the industries. The chemical fertilizers and pesticides were a big reason of this pollution. He
started looking for the solution and finally found that Organic farming could be an
appropriate solution to this problem. 

        </p>
        <p>
            In order to learn more about Organic farming, Mr. Kalpesh started visiting places like
Maharashtra and Gujarat. Finally, from his acquired knowledge and experience he created
certain formulations that could be used in place of the chemical fertilizers and pesticides.
For replacing the chemical fertilizers cow dung, compost and Amrit Paani were a good
choice. For replacing the chemical pesticides, his formulations worked as natural pesticides.
He tested those pesticides in his Uncle's farm and got very good results. Those natural
pesticides worked and could successfully replace the artificial ones. This achievement of his
got published in the newspapers and was praised by many including college lecturers. His
natural pesticides were successful in treating leaf miner and white lily buds. Slowly, farmers
near him started using those natural pesticides. But it was observed that in five years, the
pests started showing resistance to his natural pesticides. The reason to this was found that
since those farmers were also using chemical pesticides in their fields, the pests slowly
started developing that resistance. He educated the farmers not to use the chemicals but
finally had to give up since the farmers, in order to make larger profits, did not give up on
the chemical pesticides. This was a turning point in his life when he did not know what to
do. Finally, he realised that he had to make farmers aware of the philosophy of Madhyasth
Darshan (Jeevan Vidya). He realised that apart from giving those formulations to the
farmers, he had to give them a good mindset too. Only when farmers are willing to go fully
Organic that his formulations could work in a long term. He realised that the two things had
to go simultaneously - the mindset of farmers had to be changed and at the same time they
had to come up with the natural alternatives to pesticides. The philosophy of Madhyasth
Darshan helped him in changing farmer's mindsets. Madhyasth Darshan or ‘Co-existential
Thought’ is the name for a set of propositions about reality. The philosophy is not against
science but focuses on science that does not degrade the environment and connects the
people. Through this philosophy, Mr. Kalpesh was able to change the mindset of the farmers
and encourage them in producing by the Organic methodology. The work of Mr. Kalpesh is a
micro to macro work which means that he starts at the grassroot level and then takes his
work to affect the larger masses. The more he works at the ground level, the more gravity
and trust it gives to the farmers. 
        </p>
        <p>
            Apart from doing Organic farming, Mr. Kalpesh is involved in food processing and
distribution too. He produces banana biscuits, banana khakras, banana papad and banana
energy bytes. All the items produced by him are fully Organic and have better nutritional
content than the conventional ones. He uses Moringa powder and Wheat grass powder in
his cookies which makes them premium. There are two types of customers that he has –
One those who know him personally and know his nature and understand that he will not
sell products at a very high price unless they cost high and the other those who do not know
him and feel that he is selling at very high prices. Those who know him are happy even
buying a kilogram of banana chips at Rs. 1000/- as they know he is charging them correctly.
For those who do not know him, there are many times when there is an issue of the prices
of the products. The relation of Mr. Kalpesh and the people in selling the products is not of
customer and shopkeeper but of close friends. The transaction part is a very small element
of that relation. People pay him not because of shopkeeper-customer relation, but because
they know that he has put efforts to produce those products.
        </p>
