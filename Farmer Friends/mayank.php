        <h2>Mayank Agarwal</h2>
        <h3>Organic Farmer</h3><br>
        <p>
            Mayank Agrawal is an organic farmer from Baloda bazar,
Chhattisgarh and his educational
qualifications are B.E (Mechanical
engineering) from Government Engineering
College, Raipur, passed out in 2013. He was
also a very keen civil services aspirant. He is
currently residing in Singhora, a village in
Baloda Bazar district.
        </p>
        <p>
            He has 25 acres of family land in the village
and his father started a dairy farming there as
part of his meaningful social contribution after
the retirement. Mayankji started helping his
father in dairy business in 2017 and was
preparing for UPSC simultaneously. They started off with the production of A2 milk and Ghee.
The milk quality being A2 type was very good and hence they prioritized their customers to be
kids and senior citizens from Baloda Bazar and Raipur.
        </p>
        <p>
            They had a good number of cows and hence to use the waste products from the cattle, they
started making vermicompost (fertilizers, khaad) from cow dung and some other organic
products like pesticides from Urine of cows (gau mutra) and Jeevamrit . With ample amount of
fertilizers and pesticides, the family started to practice organic farming.
They cultivate scented rice (Java phool) on their farm. They also sell guavas around Baloda
Bazar at 60/- per kg which were grown on the 300 trees on their land. Currently, he is working
alongside Prakriti Organics and Farmers Pride and is a producer of paddy and rice. He gets
the price at least 1.5 times for his organic products as compared to what a chemical farmer will
get in the market. By design, Prakriti organics wants its farmers to be self-reliable but if they
wish to still be in contact its basically for services which they find it difficult to manage like -
processing, packaging, lab testing, certification, home delivery and follow up with customers.
Mayank ji feels that that the initiative might help prices in the long run.
        </p>
        <p>
            After cultivating organically produced crops he himself has also noticed changes in his family's
and his customer’s health. We all know the production methods are more hard and effort
demanding as compared to chemical farming because of unavailability of organic pesticides
and fertilizers, so they have to made by the farmer himself, but once started results in excellent
returns on investment in the long run. Due to the effort/demanding nature of the farming
method, he has also generated job opportunities on his farm and dairy business. With two full
time workers in the dairy business and 3 workers on the farm and some other workers in the
harvesting season. His message for the upcoming farmers is that even though organic farming
demand more effort, but hard work always results in happiness and increase in the goodwill of
the farmers and also the customers. He is currently busy in building his network of customers
in his locality and also starting his business of organic vegetables. He is very sensitive about
his customers’ health and is constantly working for the betterment of their health. He is very
happy from his sales on organic products and is getting a good price from his customers. Even
though the price of the organic products is slightly more as compared to chemically produced
crop, people are willing to pay the price for his efforts towards their health and the
environment. Prakriti organics is currently promoting his identity and quality so that he gets a
sustainable base of consumers by himself at the local level.
        </p>
